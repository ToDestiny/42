int		ft_sqrt(int nb)
{
	int		i;
	int		res;

	i = 0;
	res = 0;
	if (nb < 0)
		return (0);
	while ((res <= nb) && i < nb)
	{
		if ((res = i * i) == nb)
			return (i);
		i++;
	}
	return (0);
}
